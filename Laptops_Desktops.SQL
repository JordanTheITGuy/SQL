SELECT v_R_System.Netbios_Name0
	, CASE v_GS_SYSTEM_ENCLOSURE.ChassisTypes0
		WHEN 9 THEN 'LAPTOP'
		WHEN 10 THEN 'LAPTOP'
		WHEN 3 THEN 'DESKTOP'
		WHEN 6 THEN 'DESKTOP'
		WHEN 4 THEN 'DESKTOP'
		WHEN 7 THEN 'DESKTOP'
		WHEN 12 THEN 'ACCESSORY'
		WHEN 8 THEN 'LAPTOP'
		WHEN 9 THEN 'LAPTOP'
		WHEN 10 THEN 'LAPTOP'
		WHEN 11 THEN 'LAPTOP'
		WHEN 14 THEN 'LAPTOP'
		WHEN 18 THEN 'LAPTOP'
		WHEN 21 THEN 'LAPTOP'
		WHEN 30 THEN 'LAPTOP'
		WHEN 31 THEN 'LAPTOP'
		WHEN 32 THEN 'LAPTOP'
		ELSE v_GS_SYSTEM_ENCLOSURE.ChassisTypes0 + ' UNKNOWN'
		END AS 'ChasisType'
	, v_R_System.User_Name0
	, v_GS_COMPUTER_SYSTEM.Manufacturer0
	, v_GS_COMPUTER_SYSTEM.Model0
FROM v_R_System
LEFT OUTER JOIN v_GS_SYSTEM_ENCLOSURE ON v_R_System.ResourceID = v_GS_SYSTEM_ENCLOSURE.ResourceID
LEFT OUTER JOIN v_GS_COMPUTER_SYSTEM ON v_R_System.ResourceID = v_GS_COMPUTER_SYSTEM.ResourceID
WHERE v_R_System.Operating_System_Name_and0 like '%workstation%'